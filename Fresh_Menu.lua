		if _G.aimbot == false then
			_G.aimbot = true
			script.Parent.Text = "On"
			script.Parent.TextColor3 = Color3.fromRGB(136, 255, 0)
		else
			_G.aimbot = false
			script.Parent.Text = "Off"
